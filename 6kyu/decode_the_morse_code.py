def decode_morse(morse_code):
    list_words = []
    sentence = ([i.split() for i in morse_code.split("   ")])
    for word in sentence:
        for char in word:
            list_words.append(MORSE_CODE[char])
        if sentence.index(word) < len(sentence) - 1:
            list_words.append(" ")
    new_sentence = "".join(list_words).strip()
    return new_sentence


